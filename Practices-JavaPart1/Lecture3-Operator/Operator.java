package com.ctf.Operator;

public class Operator {

	public static void main(String[] args) {
		int i = 20;
		
		i++;
		i++;
		i++;
		i++;
		i++;
		
		System.out.println("The result of i++ 5 times is "+ i);
		
		i--;
		i--;
		i--;
		i--;
		i--;
		
		System.out.println("The result of i-- 5 times is " +i);
		
		float a = 4.33f;
		float b = 5.55f;
		
		System.out.println(a == 3.33 && b == 5.55);
		
		char c = 'C';
		char d = 'D';
		
		System.out.println(c == 'A' || d == 'D');

		
		
	}

}
